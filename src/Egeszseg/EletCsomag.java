package Egeszseg;

public class EletCsomag {

    private int ar;
    private int eletero;

    public EletCsomag(int ar, int eletero) {
        this.ar = ar;
        this.eletero = eletero;
    }

    public int getAr() {
        return ar;
    }

    public int getEletero() {
        return eletero;
    }

    public void setAr(int ar) {
        this.ar = ar;
    }

    public void setEletero(int eletero) {
        this.eletero = eletero;
    }
}
